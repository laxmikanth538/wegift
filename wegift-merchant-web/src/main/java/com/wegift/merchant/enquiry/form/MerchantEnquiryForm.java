package com.wegift.merchant.enquiry.form;

public class MerchantEnquiryForm {

	protected String orgName;
	protected String businessType;
	protected int yearOfEstd;
	protected String merchantLocation;
	protected String contactPerson;
	protected String contactNo;
	protected String contactEmail;
	protected String description;

	public String getOrgName() {
		return orgName;
	}

	public void setOrgName(String orgName) {
		this.orgName = orgName;
	}

	public String getBusinessType() {
		return businessType;
	}

	public void setBusinessType(String businessType) {
		this.businessType = businessType;
	}

	public int getYearOfEstd() {
		return yearOfEstd;
	}

	public void setYearOfEstd(int yearOfEstd) {
		this.yearOfEstd = yearOfEstd;
	}

	public String getMerchantLocation() {
		return merchantLocation;
	}

	public void setMerchantLocation(String merchantLocation) {
		this.merchantLocation = merchantLocation;
	}

	public String getContactPerson() {
		return contactPerson;
	}

	public void setContactPerson(String contactPerson) {
		this.contactPerson = contactPerson;
	}

	public String getContactNo() {
		return contactNo;
	}

	public void setContactNo(String contactNo) {
		this.contactNo = contactNo;
	}

	public String getContactEmail() {
		return contactEmail;
	}

	public void setContactEmail(String contactEmail) {
		this.contactEmail = contactEmail;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}
}
